'use client'

import { useState } from "react"
import { useForm } from "react-hook-form"
import { zodResolver } from "@hookform/resolvers/zod"
import { Input } from "@/shared/components/atoms/Input"
import { PrimaryBtn } from "@/shared/components/atoms/PrimaryBtn"
import { loginSchema, type LoginFormData } from "@/features/auth/schemas/loginSchema"
import { login } from "@/features/auth/api/login"

export default function LoginPage() {
    const [errorMessage, setErrorMessage] = useState("")

    const {
        register,
        handleSubmit,
        formState: { errors },
    } = useForm<LoginFormData>({
        resolver: zodResolver(loginSchema),
    })

    const onSubmit = (data: LoginFormData) => {
        setErrorMessage("")
        try {
            login(data)
        } catch {
            setErrorMessage("ログインに失敗しました")
        }
    }

    return (
        <div className="w-125 bg-white rounded-lg shadow-lg py-10">
            <form
                className="flex flex-col items-center justify-center gap-10"
                onSubmit={handleSubmit(onSubmit)}
            >
                <h1 className="text-3xl text-lime-800 font-bold text-center">
                    ログイン
                </h1>
                {errorMessage !== "" && (
                    <div className="p-5 bg-red-500 text-white w-[80%] rounded-lg">
                        {errorMessage}
                    </div>
                )}
                <div className="w-[80%]">
                    <Input
                        type="text"
                        placeholder="email"
                        {...register("email")}
                    />
                    {errors.email && (
                        <p className="text-red-500 text-sm mt-1">{errors.email.message}</p>
                    )}
                </div>
                <div className="w-[80%]">
                    <Input
                        type="password"
                        placeholder="password"
                        {...register("password")}
                    />
                    {errors.password && (
                        <p className="text-red-500 text-sm mt-1">
                            {errors.password.message}
                        </p>
                    )}
                </div>
            <PrimaryBtn onClick={() => null}>ログイン</PrimaryBtn>
            </form>
        </div>
    )
}
